//Array
const lista = ["Maça", "Pera", "Banana"]
//listar a posição 0
console.log(lista[0]);

//listar todos os itens da lista 
console.log(lista)

//adicionando uma nova fruta
lista.push("Uva");

//listar todos os itens da lista + novo item 
console.log(lista)

//comando pop para remover o ultimo item da lista
lista.pop();

console.log(lista)

//verificar tamanho da lista
console.log(lista.length);

//reordenar itens da lista
console.log(lista.reverse());

//retornar como String
console.log(lista.toString());

//adiciona elemento para separar os itens

console.log(lista.join(" - "))


